import UIKit

var TotalTimeforEgg = 180
//180 is the time represented in seconds
if TotalTimeforEgg < 180 {
    print ("the egg is undercooked and runny leave it in the pan")
}
if TotalTimeforEgg > 180 {
    print (" The egg is overcooked you might want to throw it away")
}
if TotalTimeforEgg == 180 {
    print (" Perfect timing, now take the egg out of the pan")
}
